export class Usuario{
    nombre: string;
    apellidos: string;
    pass: string;
    email: string;
    tlf: number;
    dni: string;

    constructor(){
        this.nombre = this.nombre;
        this.apellidos = this.apellidos;
        this.pass = this.pass;
        this.email = this.email;
        this.tlf = this.tlf;
        this.dni = this.dni;
    }
}